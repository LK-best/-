from docx import Document
from docx.shared import Pt
from docx.enum.text import WD_ALIGN_PARAGRAPH

# Создаем документ
document = Document()

# Заголовок
title = document.add_heading('Роль личности, общества и государства в предупреждении и ликвидации ЧС', level=1)
title.alignment = WD_ALIGN_PARAGRAPH.CENTER

# Подзаголовок
document.add_paragraph('Доклад по ОБЖ, 10 класс').alignment = WD_ALIGN_PARAGRAPH.CENTER

document.add_paragraph()

# Введение
document.add_heading('Введение', level=2)
document.add_paragraph(
    'Чрезвычайные ситуации (ЧС) представляют собой обстановку на определённой территории, '
    'сложившуюся в результате аварий, опасных природных явлений, катастроф или иных бедствий, '
    'которые могут повлечь за собой человеческие жертвы и материальный ущерб.'
)

# Роль личности
document.add_heading('Роль личности', level=2)
document.add_paragraph(
    'Каждый человек играет важную роль в предупреждении чрезвычайных ситуаций. '
    'Соблюдение правил безопасности, знание алгоритмов действий при ЧС, умение оказывать первую помощь '
    'способствуют снижению риска и минимизации последствий.'
)

# ДОБАВЛЕННЫЙ АБЗАЦ 1
document.add_paragraph(
    'Личность также обязана проявлять ответственность в повседневной жизни: соблюдать правила '
    'пожарной безопасности, дорожного движения, осторожно обращаться с электрическими приборами '
    'и газовым оборудованием. Осознанное поведение каждого человека снижает вероятность возникновения ЧС.'
)

# Роль общества
document.add_heading('Роль общества', level=2)
document.add_paragraph(
    'Общество участвует в предупреждении и ликвидации ЧС через добровольческие организации, '
    'общественные объединения и взаимодействие с органами власти. '
    'Информирование населения и коллективная ответственность повышают уровень безопасности.'
)

# Роль государства
document.add_heading('Роль государства', level=2)
document.add_paragraph(
    'Государство разрабатывает законы и нормативные акты, организует работу служб спасения, '
    'МЧС и других структур. Оно обеспечивает финансирование мероприятий по предупреждению ЧС, '
    'создаёт системы оповещения и проводит обучение населения.'
)

# ДОБАВЛЕННЫЙ АБЗАЦ 2
document.add_paragraph(
    'Кроме того, государство занимается развитием современной техники и технологий для прогнозирования '
    'и мониторинга чрезвычайных ситуаций, проводит учения и тренировки, а также оказывает помощь '
    'пострадавшим регионам при восстановлении инфраструктуры.'
)

# Заключение
document.add_heading('Заключение', level=2)
document.add_paragraph(
    'Эффективное предупреждение и ликвидация чрезвычайных ситуаций возможны только при '
    'совместных действиях личности, общества и государства.'
)

# Сохранение файла
document.save('Доклад_ОБЖ_10_класс_ЧС.docx')

print("Документ успешно создан!")